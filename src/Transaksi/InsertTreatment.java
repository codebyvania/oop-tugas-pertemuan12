    private void loadPasien() {
        cbId.removeAllItems(); // Kosongkan dulu

        EntityManager em = emf.createEntityManager();
        try {
            List<PasienKecantikanEntity> pasienList = em.createQuery(
                    "SELECT p FROM PasienKecantikanEntity p", PasienKecantikanEntity.class
            ).getResultList();

            String lastAddedId = null; // simpan ID pasien terakhir
            for (PasienKecantikanEntity pasien : pasienList) {
                cbId.addItem(pasien.getId()); // Sesuaikan getter ID
                lastAddedId = pasien.getId(); // update setiap iterasi, ID terakhir akan dipakai
            }

            // Pilih pasien terakhir secara otomatis
            if (lastAddedId != null) {
                cbId.setSelectedItem(lastAddedId);
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,
                    "Gagal memuat data pasien: " + e.getMessage(),
                    "Error",
                    JOptionPane.ERROR_MESSAGE);
        } finally {
            em.close();
        }
    }

    private void refreshPasien() {
        cbId.removeAllItems();
        EntityManager em = emf.createEntityManager();
        try {
            List<PasienKecantikanEntity> pasienList = em.createQuery(
                    "SELECT p FROM PasienKecantikanEntity p", PasienKecantikanEntity.class
            ).getResultList();

            for (PasienKecantikanEntity pasien : pasienList) {
                cbId.addItem(pasien.getId());
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,
                    "Gagal memuat data pasien: " + e.getMessage(),
                    "Error",
                    JOptionPane.ERROR_MESSAGE);
        } finally {
            em.close();
        }
    }


