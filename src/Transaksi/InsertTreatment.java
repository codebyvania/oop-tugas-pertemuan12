    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (tfIdTreatment.getText().isEmpty()
                || tfHarga.getText().isEmpty()
                || tfTanggal.getText().isEmpty()
                || cbId.getSelectedItem() == null) {
            JOptionPane.showMessageDialog(this, "Semua data harus diisi dan pasien harus dipilih!");
            return;
        }

        EntityManagerFactory emf = Persistence.createEntityManagerFactory("PBO_P12PU");
        EntityManager em = emf.createEntityManager();

        String idTreatment = tfIdTreatment.getText().trim();
        String selectedPasienId = (String) cbId.getSelectedItem();
        if (selectedPasienId == null) {
            JOptionPane.showMessageDialog(this, "Pilih pasien terlebih dahulu!");
            return;
        }

        //Cek ID Treatment spy nggk sama dengan ID Pasien
        if (idTreatment.equals(selectedPasienId)) {
            JOptionPane.showMessageDialog(this,
                    "ID Treatment dan ID Pasien tidak boleh sama!",
                    "Error",
                    JOptionPane.WARNING_MESSAGE);

            return;
        }

        String tanggalString = tfTanggal.getText().trim();

        // 1. Validasi Format Tanggal (String/VARCHAR)
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
        sdf.setLenient(false); // Memaksa format ketat

        try {
            // Lakukan parsing HANYA untuk validasi. Jika gagal, akan melempar ParseException.
            sdf.parse(tanggalString);

        } catch (java.text.ParseException e) {
            JOptionPane.showMessageDialog(this,
                    "Format tanggal salah! Gunakan yyyy-MM-dd (Contoh: 2023-12-31)",
                    "Kesalahan Input Tanggal",
                    JOptionPane.ERROR_MESSAGE);
            return;
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Terjadi kesalahan pada input tanggal: " + e.getMessage());
            return;
        }

        try {
            em.getTransaction().begin();

            // 2. Cek apakah ID Treatment sudah digunakan (Pengecekan di Level Aplikasi)
            DataTreatmentEntity existingTreatment = em.find(DataTreatmentEntity.class, idTreatment);

            if (existingTreatment != null) {
                JOptionPane.showMessageDialog(this,
                        "ID Treatment " + idTreatment + " sudah digunakan. Mohon gunakan ID lain.",
                        "Duplikasi ID",
                        JOptionPane.WARNING_MESSAGE);
                em.getTransaction().rollback();
                return;
            }

            // 3. Ambil Pasien
            PasienKecantikanEntity pasien = em.find(PasienKecantikanEntity.class, selectedPasienId);
            if (pasien == null) {
                JOptionPane.showMessageDialog(this, "Pasien dengan ID " + selectedPasienId + " tidak ditemukan!");
                em.getTransaction().rollback();
                return;
            }

            // 4. Buat dan isi objek DataTreatmentEntity
            DataTreatmentEntity treatment = new DataTreatmentEntity();
            treatment.setIdTreatment(idTreatment);
            treatment.setPasien(pasien); // Set relasi Pasien
            treatment.setDokter(cbDokter.getSelectedItem().toString());
            treatment.setNamaTreatment(tfNamaTreatment.getText().trim());

            // Konversi harga ke BigDecimal
            BigDecimal harga;
            try {
                harga = new BigDecimal(tfHarga.getText().trim());
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "Harga harus berupa angka yang valid.");
                em.getTransaction().rollback();
                return;
            }
            treatment.setHarga(harga);

            //  Set tanggal SEBAGAI STRING yang sudah divalidasi formatnya
            treatment.setTanggal(tanggalString);

            // 5. Simpan ke database
            em.persist(treatment);
            em.getTransaction().commit();

            JOptionPane.showMessageDialog(this, "Data treatment berhasil disimpan!");

            if (parentFrame != null) {
                parentFrame.showTableTreatment();
            }
            dispose();

        } catch (Exception e) {
            if (em.getTransaction().isActive()) {
                em.getTransaction().rollback();
            }

            // Error Umum Lainnya
            JOptionPane.showMessageDialog(this,
                    "Gagal menyimpan data: " + e.getMessage(),
                    "Error",
                    JOptionPane.ERROR_MESSAGE);
            logger.severe("Error: " + e.getMessage());
        } finally {
            em.close();
            // emf.close() dihindari di sini
        }
    }//GEN-LAST:event_jButton1ActionPerformed

