    private void btSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSaveActionPerformed

        EntityManager em = emf.createEntityManager(); // gunakan emf yang sudah ada di kelas

        try {
            em.getTransaction().begin();

            String idTreatment = tfIdTreatment.getText().trim();
            DataTreatmentEntity treatment = em.find(DataTreatmentEntity.class, idTreatment);

            if (treatment != null) {
                // Ambil ID pasien dari combo box
                String idPasien = cbId.getSelectedItem().toString();

                // Ambil entitas pasien berdasarkan id_pasien
                PasienKecantikanEntity pasien = em.find(PasienKecantikanEntity.class, idPasien);
                if (pasien == null) {
                    JOptionPane.showMessageDialog(this, "Pasien dengan ID tersebut tidak ditemukan!");
                    em.getTransaction().rollback();
                    return;
                }

                // Set hubungan parent (FK)
                treatment.setPasien(pasien);

                // Set data lain
                treatment.setNamaTreatment(tfTreatment.getText().trim());

                BigDecimal harga;
                try {
                    harga = new BigDecimal(tfHarga.getText().trim());
                } catch (NumberFormatException e) {
                    JOptionPane.showMessageDialog(this, "Harga harus berupa angka yang valid.");
                    em.getTransaction().rollback();
                    return;
                }
                treatment.setHarga(harga);

                String selectedIdPasien = cbId.getSelectedItem().toString();
                String selectedDokter = cbDokter.getSelectedItem().toString();
                String selectedTanggal = tfTanggal.getText().trim();

                List<DataTreatmentEntity> checkDuplicate = em.createQuery(
                        "SELECT d FROM DataTreatmentEntity d WHERE d.pasien.id = :id AND d.dokter = :dokter AND d.tanggal = :tanggal AND d.idTreatment <> :idTreatment",
                        DataTreatmentEntity.class
                )
                        .setParameter("id", selectedIdPasien)
                        .setParameter("dokter", selectedDokter)
                        .setParameter("tanggal", selectedTanggal)
                        .setParameter("idTreatment", idTreatment)
                        .getResultList();

                if (!checkDuplicate.isEmpty()) {
                    JOptionPane.showMessageDialog(this, "Data treatment untuk pasien, dokter, dan tanggal ini sudah ada!");
                    em.getTransaction().rollback();
                    return;
                }

                treatment.setDokter(cbDokter.getSelectedItem().toString());
                em.merge(treatment);
                em.getTransaction().commit();

                // Set flag updated = true agar JFrame utama tahu data berubah
                updated = true;

                JOptionPane.showMessageDialog(this, "Data treatment berhasil diperbarui!");
                dispose();
            } else {
                JOptionPane.showMessageDialog(this, "Data treatment tidak ditemukan!");
            }

        } catch (Exception e) {
            em.getTransaction().rollback();
            JOptionPane.showMessageDialog(this, "Terjadi kesalahan: " + e.getMessage());
        } finally {
            em.close();
            // jangan close emf di sini
            // emf.close();
        }
    }//GEN-LAST:event_btSaveActionPerformed

