    private void updateTreatmentFromDoctor(String dokter) {
        switch (dokter) {
            case "Dr. Anisa":
                tfTreatment.setText("Facial Premium");
                tfHarga.setText("200000");
                break;
            case "Dr. Ben":
                tfTreatment.setText("Laser Acne");
                tfHarga.setText("350000");
                break;
            case "Dr. Isabella":
                tfTreatment.setText("Botox Treatment");
                tfHarga.setText("500000");
                break;
            case "Dr. Anna":
                tfTreatment.setText("Peeling Glow");
                tfHarga.setText("250000");
                break;
            default:
                tfTreatment.setText("");
                tfHarga.setText("");
                break;
        }
    }

    private void loadTreatment(String idTreatment) {
        EntityManager em = emf.createEntityManager();

        try {
            if (idTreatment != null && !idTreatment.isEmpty()) {
                DataTreatmentEntity tr = em.find(DataTreatmentEntity.class, idTreatment);
                if (tr != null) {
                    isLoading = true;

                    // isi combo box pasien
                    cbId.removeAllItems();
                    List<PasienKecantikanEntity> pasienList = em.createQuery("SELECT p FROM PasienKecantikanEntity p", PasienKecantikanEntity.class).getResultList();
                    for (PasienKecantikanEntity p : pasienList) {
                        cbId.addItem(p.getId());
                    }

                    // isi field dari database
                    tfIdTreatment.setText(tr.getIdTreatment());
                    cbId.setSelectedItem(tr.getPasien().getId());
                    cbDokter.setSelectedItem(tr.getDokter());
                    tfTreatment.setText(tr.getNamaTreatment());
                    tfHarga.setText(tr.getHarga().toString());
                    tfTanggal.setText(tr.getTanggal().toString());

                    // form selesai load
                    isLoading = false;
                } else {
                    JOptionPane.showMessageDialog(this, "Data treatment tidak ditemukan!");
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal memuat data treatment: " + e.getMessage());
        } finally {
            em.close();
        }
    }

